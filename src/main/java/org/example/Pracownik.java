package org.example;

public class Pracownik {
    public String imie;
    public String nazwisko;
    public String dzial;
    public Integer wyplata;

    public Pracownik(String imie, String nazwisko, String dzial, Integer wyplata) {
        this.imie = imie;
        this.nazwisko = nazwisko;
        this.dzial = dzial;
        this.wyplata = wyplata;
    }
}
